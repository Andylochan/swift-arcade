import UIKit

extension UIColor {

    class var lightBlue: UIColor {
        return UIColor(red: 113.0 / 255.0, green: 197.0 / 255.0, blue: 231.0 / 255.0, alpha: 1.0)
    }

    class var lightPurple: UIColor {
        return UIColor(red: 162.0 / 255.0, green: 92.0 / 255.0, blue: 190.0 / 255.0, alpha: 1.0)
    }

    class var radialDarkestBlue: UIColor {
        return UIColor(red: 0, green: 106 / 255.0, blue: 152 / 255.0, alpha: 1)
    }

    class var radialDarkerBlue: UIColor {
        return UIColor(red: 0, green: 129 / 255.0, blue: 182 / 255.0, alpha: 1)
    }

    class var radialLighterBlue: UIColor {
        return UIColor(red: 0, green: 147 / 255.0, blue: 199 / 255.0, alpha: 1)
    }

    class var radialLightestBlue: UIColor {
        return UIColor(red: 0, green: 152 / 255.0, blue: 202 / 255.0, alpha: 1)
    }

}

